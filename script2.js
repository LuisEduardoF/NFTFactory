var nftSymbol = ""
var nftName = ""
var owner = ""

const provider = new ethers.providers.Web3Provider(window.ethereum, "goerli");
provider.send("eth_requestAccounts", []).then(() => {
    provider.listAccounts().then(async (accounts) => {
        const signer = provider.getSigner(accounts[0]);

        /* 3.1 Create instance of pet smart contract */
        NFTContract = new ethers.Contract(
            NFT_Address,
            NFT_ABI,
            signer
        );
        nftSymbol = await NFTContract.symbol()
        owner = await NFTContract.owner();
		if(owner != signer._address){
			document.getElementById("buttonADD").style.visibility = "hidden"
		}
        document.getElementById("titleName").innerHTML += " " + nftSymbol
    
        NFTContract.on('NewQuote', function (event) {
            updateQuotes(event)
        });
        
        var quotes = await NFTContract.getCollection()
        for(var i = 0; i < quotes.length; i++){
            updateQuotes(quotes[i])
        }
    });
});

var postQuote_button = document.getElementById("postQuote")

const createQuote = () => {
    // update button value
    postQuote_button.value = "Creating Quote...";

    /* 5.1 Get inputs from pet form */
    const quote = document.getElementById("quote").value;

    /* 5.3 Set pet details in smart contract */
    NFTContract.mint(quote)
        .then(() => {
        })
        .catch((err) => {
            postQuote_button.value = "Create a NIFT";
        });
};

postQuote_button.addEventListener("click", createQuote)

last_update_counter = 0
function updateQuotes(tokenURI) {
        var col = document.createElement('col');
        col.innerHTML = `
        <figure class="bg-light p-4"
        style="border-left: .35rem solid #fcdb5e; border-top: 1px solid #eee; border-right: 1px solid #eee; border-bottom: 1px solid #eee;">
            <blockquote class="blockquote pb-2">
                <p id="quote_${last_update_counter}">
                    ${"\"" + tokenURI + "\""}
                </p>
            </blockquote>
            <figcaption class="blockquote-footer mb-0" id="owner">
                ${owner}
            </figcaption>
        </figure>`;
        document.getElementById('row').appendChild(col);
        last_update_counter +=1
}




